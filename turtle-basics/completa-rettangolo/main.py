# uso le funzioni della libreria turtle
import turtle                

# creo una tartaruga chiamata pippo
pippo = turtle.Turtle()

# dico a pippo di muoversi in avanti di 150 unita'
pippo.forward(150)

# ruota di 90 gradi a sinistra
pippo.left(90)

# disegno il secondo lato del rettangolo
pippo.forward(75)

